<template>

    <div id="app">

      <router-view></router-view>

    </div>

</template>

<script>

  import { mapGetters } from 'vuex'

export default {
  name: 'App',
  computed: {
    ...mapGetters({ currentUser: 'currentUser' })
  },
  created () {
    this.checkCurrentLogin()
  },
  updated () {
    this.checkCurrentLogin()
  },
  methods: {
    checkCurrentLogin () {
      if (!this.currentUser && this.$route.path !== '/') {
        this.$router.push('/login')
      }
    }
  },
}

</script>

